function months(month){
    switch (month) {
        case 1:
            console.log(`January`)
            break;
        case 2:
            console.log(`February`)
            break;
        case 3:
            console.log(`March`)
            break;
        case 4:
            console.log(`April`)
            break;
    
        default:
            console.log(`Defult Case Match`)
            break;
    }
}
months(1)
months(4)
months("April")


